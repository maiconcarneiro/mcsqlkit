COL TASK_ID                             HEADING 'Task|ID'  FORMAT 999999
COL INMEMORY_SIZE                       HEADING 'InMemory|Size'  FORMAT 999,999,999
COL ESTIMATED_DB_TIME_LOW               HEADING 'Estimated|DB Time (Low)'  FORMAT 999999
COL ESTIMATED_DB_TIME_HIGH              HEADING 'Estimated|DB Time (High)'  FORMAT 999999
COL ESTIMATED_DB_TIME_ANALYTICS_LOW     HEADING 'Estimated|DB Time Analytics | (Low)'  FORMAT 999999
COL ESTIMATED_DB_TIME_ANALYTICS_HIGH    HEADING 'Estimated|DB Time Analytics | (High)'  FORMAT 999999
