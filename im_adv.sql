       INMEMORY_SIZE/1024/1024 INMEMORY_SIZE,
       ESTIMATED_DB_TIME_LOW,
       ESTIMATED_DB_TIME_HIGH,
       ESTIMATED_DB_TIME_ANALYTICS_LOW,
       ESTIMATED_DB_TIME_ANALYTICS_HIGH,
       RECOMMENDED_OBJ_LIST
